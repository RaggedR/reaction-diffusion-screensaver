# Generation algorithms
# ---------------------------------------------------------------------------

def gen_dfs(rows, cols):
    """Recursive backtracker — long winding corridors."""
    maze = Maze(rows, cols)
    visited = {(0, 0)}
    stack = [(0, 0)]
    while stack:
        r, c = stack[-1]
        nbs = [(nr, nc) for nr, nc in maze.neighbors(r, c) if (nr, nc) not in visited]
        if nbs:
            nr, nc = random.choice(nbs)
            maze.add_edge((r, c), (nr, nc))
            visited.add((nr, nc))
            stack.append((nr, nc))
        else:
            stack.pop()
    return maze


def gen_kruskal(rows, cols):
    """Randomized Kruskal's — short branchy passages."""
    maze = Maze(rows, cols)
    all_edges = []
    for r in range(rows):
        for c in range(cols):
            if c + 1 < cols:
                all_edges.append(((r, c), (r, c + 1)))
            if r + 1 < rows:
                all_edges.append(((r, c), (r + 1, c)))
    random.shuffle(all_edges)

    parent = {}
    rank = {}
    for r in range(rows):
        for c in range(cols):
            parent[(r, c)] = (r, c)
            rank[(r, c)] = 0

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(x, y):
        rx, ry = find(x), find(y)
        if rx == ry:
            return False
        if rank[rx] < rank[ry]:
            rx, ry = ry, rx
        parent[ry] = rx
        if rank[rx] == rank[ry]:
            rank[rx] += 1
        return True

    for u, v in all_edges:
        if union(u, v):
            maze.add_edge(u, v)
    return maze


def gen_wilson(rows, cols):
    """Wilson's algorithm — uniform random spanning tree."""
    maze = Maze(rows, cols)
    in_tree = {(0, 0)}
    cells = [(r, c) for r in range(rows) for c in range(cols)]

    for start in cells:
        if start in in_tree:
            continue
        path = [start]
        pos = {start: 0}
        cur = start
        while cur not in in_tree:
            nxt = random.choice(list(maze.neighbors(*cur)))
            if nxt in pos:
                idx = pos[nxt]
                for removed in path[idx + 1:]:
                    del pos[removed]
                path = path[:idx + 1]
                cur = nxt
            else:
                path.append(nxt)
                pos[nxt] = len(path) - 1
                cur = nxt
        for i in range(len(path) - 1):
            maze.add_edge(path[i], path[i + 1])
            in_tree.add(path[i])
        if path:
            in_tree.add(path[-1])
    return maze


def gen_prim(rows, cols):
    """Randomized Prim's — radiating growth."""
    maze = Maze(rows, cols)
    in_maze = {(0, 0)}
    frontier = []
    for nb in maze.neighbors(0, 0):
        frontier.append(((0, 0), nb))

    while frontier:
        idx = random.randrange(len(frontier))
        frontier[idx], frontier[-1] = frontier[-1], frontier[idx]
        u, v = frontier.pop()
        if v in in_maze:
            continue
        maze.add_edge(u, v)
        in_maze.add(v)
        for nb in maze.neighbors(*v):
            if nb not in in_maze:
                frontier.append((v, nb))
    return maze


