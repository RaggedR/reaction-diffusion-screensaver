def maze_tikz(maze, cell=0.5, solution=True, tree_edges=False, label=None):
    """Render a maze as TikZ."""
    R, C = maze.rows, maze.cols
    lines = []
    lines.append(f"\\begin{{tikzpicture}}[x={cell}cm, y={cell}cm]")

    # Spanning tree edges (light blue behind walls)
    if tree_edges:
        lines.append("  % tree edges")
        for (r1, c1), (r2, c2) in maze.edges:
            x1, y1 = c1 + 0.5, R - r1 - 0.5
            x2, y2 = c2 + 0.5, R - r2 - 0.5
            lines.append(f"  \\draw[blue!25, line width=2.5pt] ({x1:.1f},{y1:.1f}) -- ({x2:.1f},{y2:.1f});")

    # Outer border with entry (top-left) and exit (bottom-right)
    lines.append("  % border")
    # Top
    lines.append(f"  \\draw[line width=1pt] (0,{R}) -- ({C},{R});")
    # Bottom
    lines.append(f"  \\draw[line width=1pt] (0,0) -- ({C},0);")
    # Left: gap at row 0 (top)
    lines.append(f"  \\draw[line width=1pt] (0,0) -- (0,{R - 1});")
    # Right: gap at row R-1 (bottom)
    lines.append(f"  \\draw[line width=1pt] ({C},1) -- ({C},{R});")

    # Entry/exit arrows
    lines.append(f"  \\draw[red!70!black, thick, ->, >=stealth] (-0.35,{R - 0.5:.1f}) -- (0.25,{R - 0.5:.1f});")
    lines.append(f"  \\draw[red!70!black, thick, ->, >=stealth] ({C - 0.25:.1f},0.5) -- ({C + 0.35:.1f},0.5);")

    # Internal horizontal walls (between row r and r+1)
    lines.append("  % internal walls")
    for r in range(R - 1):
        y = R - r - 1
        for c in range(C):
            if not maze.has_passage((r, c), (r + 1, c)):
                lines.append(f"  \\draw[line width=1pt] ({c},{y}) -- ({c + 1},{y});")

    # Internal vertical walls (between col c and c+1)
    for r in range(R):
        y = R - r
        for c in range(C - 1):
            if not maze.has_passage((r, c), (r, c + 1)):
                lines.append(f"  \\draw[line width=1pt] ({c + 1},{y}) -- ({c + 1},{y - 1});")

    # Solution path
    if solution:
        path = maze.solve((0, 0), (R - 1, C - 1))
        if path:
            lines.append("  % solution")
            coords = " -- ".join(f"({c + 0.5:.1f},{R - r - 0.5:.1f})" for r, c in path)
            lines.append(f"  \\draw[red!50!black, line width=0.8pt, opacity=0.5] {coords};")

    # Label
    if label:
        lines.append(f"  \\node[below] at ({C / 2:.1f},-0.3) {{\\footnotesize {label}}};")

    lines.append("\\end{tikzpicture}")
    return "\n".join(lines)


def stats_line(maze):
    """One-line LaTeX summary of tree statistics."""
    deg = maze.degree_map()
    dist = Counter(deg.values())
    n = maze.rows * maze.cols
    leaves = dist.get(1, 0)
    junctions = sum(v for k, v in dist.items() if k >= 3)
    path = maze.solve((0, 0), (maze.rows - 1, maze.cols - 1))
    sol_len = len(path) - 1 if path else 0
    shortest = maze.rows + maze.cols - 2
    tort = sol_len / shortest if shortest > 0 else 0
    deg_str = ", ".join(f"$d\\!=\\!{k}$:\\,{v}" for k, v in sorted(dist.items()))
    return (
        f"Leaves: {leaves} \\quad "
        f"Junctions: {junctions} \\quad "
        f"Solution: {sol_len} steps \\quad "
        f"Tortuosity: {tort:.2f}\\\\\n"
        f"Degree distribution: {deg_str}"
    )


def generate_document(size=8, seed=42):
    algorithms = [
        ("DFS", gen_dfs),
        ("Kruskal", gen_kruskal),
        ("Wilson", gen_wilson),
        ("Prim", gen_prim),
    ]

    cell = 0.7

    doc = []
    doc.append(r"""\documentclass[border=5mm]{standalone}
\usepackage{tikz}
\usepackage[T1]{fontenc}
\usepackage{lmodern}

\begin{document}
\begin{tikzpicture}
""")

    spacing = size * cell + 2.0  # horizontal gap between mazes

    for i, (name, gen_func) in enumerate(algorithms):
        random.seed(seed + i)
        maze = gen_func(size, size)
        xoff = i * spacing

        doc.append(f"  \\begin{{scope}}[shift={{({xoff:.1f},0)}}]")
        # Maze walls
        R, C = maze.rows, maze.cols
        # Outer border
        doc.append(f"    \\draw[line width=1pt] (0,{R*cell}) -- ({C*cell},{R*cell});")
        doc.append(f"    \\draw[line width=1pt] (0,0) -- ({C*cell},0);")
        doc.append(f"    \\draw[line width=1pt] (0,0) -- (0,{(R-1)*cell});")
        doc.append(f"    \\draw[line width=1pt] ({C*cell},{cell}) -- ({C*cell},{R*cell});")
        # Entry/exit arrows
        doc.append(f"    \\draw[red!70!black, thick, ->, >=stealth] (-0.25,{(R-0.5)*cell:.2f}) -- (0.2,{(R-0.5)*cell:.2f});")
        doc.append(f"    \\draw[red!70!black, thick, ->, >=stealth] ({(C-0.3)*cell:.2f},{0.5*cell:.2f}) -- ({C*cell+0.25:.2f},{0.5*cell:.2f});")
        # Internal walls
        for r in range(R - 1):
            y = (R - r - 1) * cell
            for c in range(C):
                if not maze.has_passage((r, c), (r + 1, c)):
                    doc.append(f"    \\draw[line width=1pt] ({c*cell},{y}) -- ({(c+1)*cell},{y});")
        for r in range(R):
            y_top = (R - r) * cell
            y_bot = (R - r - 1) * cell
            for c in range(C - 1):
                if not maze.has_passage((r, c), (r, c + 1)):
                    doc.append(f"    \\draw[line width=1pt] ({(c+1)*cell},{y_top}) -- ({(c+1)*cell},{y_bot});")
        # Solution path
        path = maze.solve((0, 0), (R - 1, C - 1))
        if path:
            coords = " -- ".join(f"({(c+0.5)*cell:.2f},{(R-r-0.5)*cell:.2f})" for r, c in path)
            doc.append(f"    \\draw[red!50!black, line width=0.7pt, opacity=0.45] {coords};")
        # Label
        doc.append(f"    \\node[below, font=\\small\\bfseries] at ({C*cell/2:.2f},-0.3) {{{name}}};")
        doc.append(f"  \\end{{scope}}")
        doc.append("")

    doc.append(r"\end{tikzpicture}")
    doc.append(r"\end{document}")
    return "\n".join(doc)


if __name__ == "__main__":
    size = int(sys.argv[1]) if len(sys.argv) > 1 else 8
    seed = int(sys.argv[2]) if len(sys.argv) > 2 else 42

    latex = generate_document(size=size, seed=seed)
    tex_path = "mazes.tex"
    with open(tex_path, "w") as f:
        f.write(latex)
    print(f"Written {tex_path}")

    result = subprocess.run(
        ["pdflatex", "-interaction=nonstopmode", tex_path],
        capture_output=True, text=True
    )
    if result.returncode == 0:
        print("Compiled mazes.pdf")
    else:
        print("pdflatex failed:")
        print(result.stdout[-2000:])
        sys.exit(1)
