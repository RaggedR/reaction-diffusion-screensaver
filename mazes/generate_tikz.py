#!/usr/bin/env python3
"""Generate perfect mazes as TikZ/LaTeX and compile to PDF.

Each maze is a spanning tree of an m×n grid graph, generated by
four different algorithms that produce structurally distinct trees.
"""

import random
import subprocess
import sys
from collections import Counter, defaultdict, deque


class Maze:
    """Perfect maze = spanning tree of the grid graph G_{rows,cols}."""

    def __init__(self, rows, cols):
        self.rows = rows
        self.cols = cols
        self.edges = set()  # {((r1,c1),(r2,c2))} with (r1,c1) < (r2,c2)

    def neighbors(self, r, c):
        for dr, dc in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            nr, nc = r + dr, c + dc
            if 0 <= nr < self.rows and 0 <= nc < self.cols:
                yield (nr, nc)

    def add_edge(self, u, v):
        self.edges.add((min(u, v), max(u, v)))

    def has_passage(self, u, v):
        return (min(u, v), max(u, v)) in self.edges

    def adjacency(self):
        adj = defaultdict(list)
        for u, v in self.edges:
            adj[u].append(v)
            adj[v].append(u)
        return adj

    def solve(self, start, end):
        adj = self.adjacency()
        parent = {start: None}
        queue = deque([start])
        while queue:
            node = queue.popleft()
            if node == end:
                path = []
                while node is not None:
                    path.append(node)
                    node = parent[node]
                return path[::-1]
            for nb in adj[node]:
                if nb not in parent:
                    parent[nb] = node
                    queue.append(nb)
        return []

    def degree_map(self):
        deg = defaultdict(int)
        for u, v in self.edges:
            deg[u] += 1
            deg[v] += 1
        return deg


